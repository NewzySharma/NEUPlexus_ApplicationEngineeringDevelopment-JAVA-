    private void populateCombo(){
        organizationJComboBox.removeAllItems();
        
        for (Type type : UserOrganization.Type.values()) {
            switch (enterprise.getEnterpriseType().getValue()) {
                case "AccomodationPortal":
                    if (!type.getValue().equals(Type.Admin.getValue()) && !type.getValue().equals(Type.Alumni.getValue()) && !type.getValue().equals(Type.Manager.getValue())) {
                        organizationJComboBox.addItem(type);
                    }
                    break;
                case "CarPool":
                    if (!type.getValue().equals(Type.Admin.getValue()) && !type.getValue().equals(Type.IncomingStudent.getValue()) && !type.getValue().equals(Type.Manager.getValue())) {
                        organizationJComboBox.addItem(type);
                    }
                    break;
                case "Management":
                    if (type.getValue().equals(Type.Manager.getValue())) {
                        organizationJComboBox.addItem(type);
                    }
                    break;
                case "QnAPortal":
                    if (!type.getValue().equals(Type.Admin.getValue()) && !type.getValue().equals(Type.Manager.getValue())) {
                        organizationJComboBox.addItem(type);
                    }
                    break;
            }

        }
    }

    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        
        model.setRowCount(0);
        
        for (UserOrganization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization.getOrganizationID();
            row[1] = organization.getName();
            
            model.addRow(row);
        }
    }
