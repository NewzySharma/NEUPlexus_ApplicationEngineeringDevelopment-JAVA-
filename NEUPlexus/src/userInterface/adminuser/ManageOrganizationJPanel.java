    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed

        Type type = (Type) organizationJComboBox.getSelectedItem();
        UserOrganization uo=enterprise.getOrganizationDirectory().createOrganization(type);
        if(uo==null){
              JOptionPane.showMessageDialog(null, "Organization already present!","ERROR",JOptionPane.ERROR);
              logger.error("Exception while creating organization: organization already present");
              return;
        }
        populateTable();
        logger.info("Organization "+uo.getName()+" added successfully");
    }//GEN-LAST:event_addJButtonActionPerformed

