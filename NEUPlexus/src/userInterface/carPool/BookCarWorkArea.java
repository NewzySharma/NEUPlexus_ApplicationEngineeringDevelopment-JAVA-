    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
     int selected = carListTable.getSelectedRow();
        if(selected>=0){
            CarInfo q= (CarInfo)carListTable.getValueAt(selected, 0);
            CarDirectory qdir = datastore.getCardir();
            if (JOptionPane.showConfirmDialog(null, "Are you sure?", "WARNING",
        JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
    qdir.getCarList().remove(q);
            JOptionPane.showMessageDialog(null, "Car Listing deleted successfully!","SUCCESS",JOptionPane.INFORMATION_MESSAGE);
            EmailClient.sendEmail(q.getPostingUserName(), "Car listing deleted", "You have successfullly deleted car listing from portal.");
            if(q.getBookingUserNames().size()!=0){
                for(BookingDetails b:q.getBookingUserNames())
                EmailClient.sendEmail(b.getUserID(), "Car listing removed", "User "+q.getPostingUserName()+" removed car listing from portal. Your booking is automatically canceled with the user.");
            }
            searchCar();
            printCarListTable();
            logger.info("Car listing deleted by user "+NeuPlexusHome.useraccount.getUserName());
} else {
    // no option
}
            
            
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void carListTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_carListTableMouseClicked
        int selected = carListTable.getSelectedRow();
        if(selected>=0){
            CarInfo q= (CarInfo)carListTable.getValueAt(selected, 0);
            if(q.getPostingUserName().equalsIgnoreCase(NeuPlexusHome.useraccount.getUserName())){
                btnDelete.setEnabled(true);
            }else{
                btnDelete.setEnabled(false);
            }
        }
    }//GEN-LAST:event_carListTableMouseClicked


