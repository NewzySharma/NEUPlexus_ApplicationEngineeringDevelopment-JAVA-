    private void printCarListTable() {
        int i = 1;
        btnDelete.setVisible(false);
        CarDirectory cdir = datastore.getCardir();
        DefaultTableModel m = (DefaultTableModel) carListTable.getModel();
        m.setRowCount(0);
        for (CarInfo c : cdir.getCarList()) {
            m.addRow(new Object[]{c,c.getCarName(), c.getStartPoint(), c.getDropOffPoint(), c.getOccupancy()});
        }

    }

    private void searchCar() {
        carList.clear();
         for (CarInfo info : datastore.getCardir().getCarList()) {
                if (jtxtFromLocation.getText().equalsIgnoreCase(info.getStartPoint()) && jtxtToLocation.getText().equalsIgnoreCase(info.getDropOffPoint())) {
                   if(!info.isBooked())
                    carList.add(info);
                }
        }
    }

