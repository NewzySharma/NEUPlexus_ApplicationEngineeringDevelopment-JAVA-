    private void printBookingListTable() {
       CarDirectory cdir = datastore.getCardir();
       
        DefaultTableModel m = (DefaultTableModel) carBookTable.getModel();
          m.setRowCount(0);
        for(CarInfo c:cdir.getCarList()){
            if(c.getBookingUserNames().size()!=0){
                if(c.getPostingUserName().equalsIgnoreCase(NeuPlexusHome.useraccount.getUserName()) ){
                    for(BookingDetails b : c.getBookingUserNames())
                     m.addRow(new Object[]{b.getBookingId(),c.getCarName(),b, b.getDate(),b.getTime()});
                }
                else {
                    for(BookingDetails book : c.getBookingUserNames()){
                        if(book.getUserID().equalsIgnoreCase(NeuPlexusHome.useraccount.getUserName())){
                            for(BookingDetails b : c.getBookingUserNames())
                              m.addRow(new Object[]{b.getBookingId(),c.getCarName(),b, b.getDate(),b.getTime()});
                            break;
                        }
                    }
                }
            }
        }
      
    }

