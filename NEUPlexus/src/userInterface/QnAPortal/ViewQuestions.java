    private void questionsTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_questionsTableMouseClicked
       int selected = questionsTable.getSelectedRow();
        if(selected>=0){
            Question q= (Question)questionsTable.getValueAt(selected, 0);
            if(q.getAskedByEmailId().equalsIgnoreCase(NeuPlexusHome.useraccount.getUserName())){
                btnDelete.setEnabled(true);
            }else{
                btnDelete.setEnabled(false);
            }
        }
    }//GEN-LAST:event_questionsTableMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try{
        int selected = questionsTable.getSelectedRow();
        if(selected>=0){
            Question q= (Question)questionsTable.getValueAt(selected, 0);
            QuestionDirectory qdir = datastore.getQuesdir();
            qdir.getQuestionList().remove(q);
            JOptionPane.showMessageDialog(null, "Question deleted successfully!","SUCCESS",JOptionPane.INFORMATION_MESSAGE);
            printQuestionsTable();
        }
        }catch(Exception e){
            logger.error("Exception while deleting question "+e.getStackTrace());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


    public void printQuestionsTable() {
        try{
        btnDelete.setEnabled(false);
        QuestionDirectory qdir = datastore.getQuesdir();
        DefaultTableModel m = (DefaultTableModel)questionsTable.getModel();
        m.setRowCount(0);
        for(Question q:qdir.getQuestionList()){
           m.addRow(new Object[]{q,q.getQuestionType(),q.getQuestionText(),q.getNumberOfAnswers()});
        }
        }catch(Exception e){
            logger.error("Exception while printing question table "+e.getStackTrace());
        }
    }
    
