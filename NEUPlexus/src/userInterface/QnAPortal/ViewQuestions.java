    private void viewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnActionPerformed
        try{
        int selected = questionsTable.getSelectedRow();
        if(selected>=0){
         Question q= (Question)questionsTable.getValueAt(selected, 0);
        panelRight.add("viewQuestion", new ViewQuestionJPanel(panelRight,q));
        CardLayout layout = (CardLayout)panelRight.getLayout();
           layout.next(panelRight);
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select any row to display","Error",JOptionPane.ERROR_MESSAGE);
        }                        
        }catch(Exception e){
            logger.error("Exception in view question "+e.getStackTrace());
        }
    }//GEN-LAST:event_viewBtnActionPerformed

